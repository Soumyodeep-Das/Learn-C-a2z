#include<stdio.h>

int main(){
    printf("This a programme to show all the escape sequences of C language.\n");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe first Escape sequence is Front slash a");
    printf("\nIt's name is Alarm or Beep");
    printf("\nIt makes a beep sound when used.");
    printf("\nExample:--");
    printf("\nMy\aname\ais\aSoumyodeep\aDas");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe second Escape sequence is Front slash b");
    printf("\nIt's name is Backspace");
    printf("\nIt is used to delete a letter before from its presence");
    printf("\nExample:--");
    printf("\nMy\bname\bis\bSoumyodeep\bDas");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe third Escape sequence is Front slash f");
    printf("\nIt's name is Form Feed");
    printf("\nIt is used to print words into a vertical format.");
    printf("\nExample:--");
    printf("\nMy\fname\fis\fSoumyodeep\fDas");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe fourth Escape sequence is Front slash n");
    printf("\nIt's name is New Line");
    printf("\nIt is used to make a new line.");
    printf("\nExample:--");
    printf("\nMy\nname\nis\nSoumyodeep\nDas");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe fifth Escape sequence is Front slash r");
    printf("\nIt's name is Carriage Return");
    printf("\nIt is used to overlap words over and over.");
    printf("\nExample:--");
    printf("\nMy\rname\ris\rSoumyodeep\rDas");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe sixth Escape sequence is Front slash t");
    printf("\nIt's name is Horizontal Tab");
    printf("\nIt is used to print a horizontal tab between two words.");
    printf("\nExample:--");
    printf("\nMy\tname\tis\tSoumyodeep\tDas");
    //-----------------------------------------------------------------------------------------------//
    printf("\nThe seventh Escape sequence is Front slash v");
    printf("\nIt's name is Vertical Tab");
    printf("\nIt is used to print a vertical tab between two words. Just like Form Feed, but its mechanism is different.");
    printf("\nExample:--");
    printf("\nMy\vname\vis\vSoumyodeep\vDas\n");
    
    return 0;
}